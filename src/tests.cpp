using namespace std;

bool Tests::test_PredictUKF( void ){
    bool passed = true;
    UKF ukf;
    ukf.verbose_ = true;
    ukf.x_ <<   5.7441,
                1.3800,
                2.2049,
                0.5015,
                0.3528; 
    ukf.P_  <<   0.0043,   -0.0013,    0.0030,   -0.0022,   -0.0020,
                -0.0013,    0.0077,    0.0011,    0.0071,    0.0060,
                 0.0030,    0.0011,    0.0054,    0.0007,    0.0008,
                -0.0022,    0.0071,    0.0007,    0.0098,    0.0100,
                -0.0020,    0.0060,    0.0008,    0.0100,    0.0123;
    ukf.std_a_ = 0.2;          
    ukf.std_yawdd_ = 0.2;
    cout << "***running PredictUKF***"<<endl;
    ukf.PredictUKF(1.0);

    MatrixXd expected_x_sig = MatrixXd(7,15);
    expected_x_sig << 
        5.7441,5.85768,5.7441,5.7441,5.7441,5.7441,5.7441,5.7441,5.63052,5.7441,5.7441,5.7441,5.7441,5.7441,5.7441,
        1.38,1.34566,1.52806,1.38,1.38,1.38,1.38,1.38,1.41434,1.23194,1.38,1.38,1.38,1.38,1.38,
        2.2049,2.28414,2.24557,2.29582,2.2049,2.2049,2.2049,2.2049,2.12566,2.16423,2.11398,2.2049,2.2049,2.2049,2.2049,
        0.5015,0.44339,0.631886,0.516923,0.595227,0.5015,0.5015,0.5015,0.55961,0.371114,0.486077,0.407773,0.5015,0.5015,0.5015,
        0.3528,0.299973,0.462123,0.376339,0.48417,0.418721,0.3528,0.3528,0.405627,0.243477,0.329261,0.22143,0.286879,0.3528,0.3528,
        0,0,0,0,0,0,0.34641,0,0,0,0,0,0,-0.34641,0,
        0,0,0,0,0,0,0,0.34641,0,0,0,0,0,0,-0.34641;
    cout << "***Comparing output***"<<endl;
    for(int i=0; i < 7; ++i)
    {
        for(int d=0; d < 15; ++d)
        {
            if( round(expected_x_sig(i,d)*1000) != round(ukf.Xsig_gen_(i,d)*1000) )
            {
                cout<<"expected_x_sig("<<i<<","<<d<<") does not equal ukf.Xsig_gen_("
                    <<i<<","<<d<<")"<<endl;
                cout << expected_x_sig(i,d) << " != " << ukf.Xsig_gen_(i,d)<<endl;
                passed = false;
            }
        }
    }

    return passed;
}